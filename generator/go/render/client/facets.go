// defaultContainerKey derives the JSON container key from the facet name when
// the schema file doesn't provide an explicit top-level property key.
// Strip "Facet" suffix, then strip kind suffix, then lowercase first char.
func defaultContainerKey(name string, kind discover.FacetType) string {
	base := strings.TrimSuffix(name, "Facet")
	switch kind {
	case discover.FacetJob:
		base = strings.TrimSuffix(base, "Job")
	case discover.FacetDataset:
		base = strings.TrimSuffix(base, "Dataset")
	case discover.FacetRun:
		base = strings.TrimSuffix(base, "Run")
	case discover.FacetInputDataset:
		base = strings.TrimSuffix(base, "InputDataset")
	case discover.FacetOutputDataset:
		base = strings.TrimSuffix(base, "OutputDataset")
	}
	return genutil.LowerFirst(base)
}

func facetsByKind(facets []ir.Facet, kind discover.FacetType) []ir.Facet {
	var out []ir.Facet
	for _, f := range facets {
		if f.Kind == kind {
			out = append(out, f)
		}
	}
	sort.Slice(out, func(i, j int) bool { return out[i].Name < out[j].Name })
	return out
}

func sortedFacets(facets []ir.Facet) []ir.Facet {
	out := make([]ir.Facet, len(facets))
	copy(out, facets)
	sort.Slice(out, func(i, j int) bool {
		if out[i].Kind == out[j].Kind {
			return out[i].Name < out[j].Name
		}
		return string(out[i].Kind) < string(out[j].Kind)
	})
	return out
}
