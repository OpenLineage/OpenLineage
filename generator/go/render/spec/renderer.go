// Package spec generates the OpenLineage core event and entity types from OpenLineage.json.
package spec

import (
	"fmt"
	"sort"
	"strings"

	"github.com/atombender/go-jsonschema/pkg/schemas"

	"github.com/OpenLineage/openlineage/generator/go/genutil"
)

// schemaToGoName maps $defs names that differ from their Go type names.
var schemaToGoName = map[string]string{
	"Run":           "Run",
	"InputDataset":  "InputElement",
	"OutputDataset": "OutputElement",
	"StaticDataset": "Dataset",
}

// facetContainerMap maps a facet base-type $defs name to the typed Go container.
var facetContainerMap = map[string]string{
	"RunFacet":           "*facets.RunFacets",
	"JobFacet":           "*facets.JobFacets",
	"DatasetFacet":       "*facets.DatasetFacets",
	"InputDatasetFacet":  "*facets.InputDatasetFacets",
	"OutputDatasetFacet": "*facets.OutputDatasetFacets",
}

// RenderSpec generates the content of spec.gen.go from OpenLineage.json.
func RenderSpec(schema *schemas.Schema) string {
	defs := schema.Definitions
	var b strings.Builder

	b.WriteString(`/*
 * Copyright 2018-2026 contributors to the OpenLineage project
 * SPDX-License-Identifier: Apache-2.0
 */
// Code generated by facet-gen. DO NOT EDIT.

package openlineage

import (
	"time"

	"github.com/OpenLineage/openlineage/client/go/pkg/facets"
)

`)

	emitEventTypeEnum(&b, defs)
	emitMergedEventStruct(&b, defs)

	for _, entry := range []struct{ defName, goName string }{
		{"Run", "Run"},
		{"Job", "Job"},
		{"StaticDataset", "Dataset"},
		{"InputDataset", "InputElement"},
		{"OutputDataset", "OutputElement"},
	} {
		def := defs[entry.defName]
		if def == nil {
			continue
		}
		emitStruct(&b, entry.goName, def.Description, collectProperties(def, defs))
	}

	return b.String()
}

// ── Internal types ────────────────────────────────────────────────────────────

type fieldSpec struct {
	jsonName    string
	goName      string
	goType      string
	required    bool
	description string
}

// ── Emitters ──────────────────────────────────────────────────────────────────

func emitEventTypeEnum(b *strings.Builder, defs map[string]*schemas.Type) {
	runEvent := defs["RunEvent"]
	if runEvent == nil {
		return
	}
	var enumProp *schemas.Type
	for _, sub := range runEvent.AllOf {
		if p, ok := sub.Properties["eventType"]; ok {
			enumProp = p
			break
		}
	}
	if enumProp == nil || len(enumProp.Enum) == 0 {
		return
	}

	if enumProp.Description != "" {
		desc := oneLine(enumProp.Description)
		fmt.Fprintf(b, "// EventType — %s\n", desc)
	} else {
		b.WriteString("// EventType is an OpenLineage run state transition.\n")
	}
	b.WriteString("type EventType string\n\n")
	b.WriteString("const (\n")
	for _, v := range enumProp.Enum {
		s, ok := v.(string)
		if !ok {
			continue
		}
		// Title-case the suffix: "ABORT" → "Abort"
		suffix := strings.ToUpper(s[:1]) + strings.ToLower(s[1:])
		fmt.Fprintf(b, "\tEventType%s EventType = %q\n", suffix, s)
	}
	b.WriteString(")\n\n")
}

// emitMergedEventStruct emits the combined Event struct from BaseEvent +
// RunEvent + DatasetEvent + JobEvent. Fields from BaseEvent are required;
// all event-specific fields are optional (any given event may lack them).
func emitMergedEventStruct(b *strings.Builder, defs map[string]*schemas.Type) {
	required := map[string]bool{}
	props := map[string]*schemas.Type{}

	if base := defs["BaseEvent"]; base != nil {
		for _, r := range base.Required {
			required[r] = true
		}
		for k, v := range base.Properties {
			props[k] = v
		}
	}

	for _, eventName := range []string{"RunEvent", "DatasetEvent", "JobEvent"} {
		def := defs[eventName]
		if def == nil {
			continue
		}
		for _, sub := range def.AllOf {
			if sub.Ref != "" {
				continue // skip $ref to BaseEvent
			}
			for k, v := range sub.Properties {
				if _, exists := props[k]; !exists {
					props[k] = v
				}
			}
		}
	}

	var fields []fieldSpec
	for jsonName, prop := range props {
		isReq := required[jsonName]
		fields = append(fields, fieldSpec{
			jsonName:    jsonName,
			goName:      genutil.FormatGoFieldName(export(jsonName)),
			goType:      propGoType(prop, isReq),
			required:    isReq,
			description: description(prop),
		})
	}
	sortFields(fields)

	b.WriteString("// Event represents an OpenLineage event.\n")
	b.WriteString("type Event struct {\n")
	for _, f := range fields {
		emitField(b, f)
	}
	b.WriteString("}\n\n")
}

func emitStruct(b *strings.Builder, goTypeName, desc string, fields []fieldSpec) {
	if desc != "" {
		fmt.Fprintf(b, "// %s — %s\n", goTypeName, oneLine(desc))
	} else {
		fmt.Fprintf(b, "// %s\n", goTypeName)
	}
	fmt.Fprintf(b, "type %s struct {\n", goTypeName)
	for _, f := range fields {
		emitField(b, f)
	}
	b.WriteString("}\n\n")
}

func emitField(b *strings.Builder, f fieldSpec) {
	if f.description != "" {
		fmt.Fprintf(b, "\t// %s\n", oneLine(f.description))
	}
	fmt.Fprintf(b, "\t%s %s `%s`\n", f.goName, f.goType, jsonTag(f.jsonName, f.required))
}

func jsonTag(jsonName string, required bool) string {
	if required {
		return fmt.Sprintf("json:%q", jsonName)
	}
	return fmt.Sprintf(`json:"%s,omitempty"`, jsonName)
}

// ── Property collection ───────────────────────────────────────────────────────

// collectProperties gathers all properties from a def, following allOf chains.
func collectProperties(t *schemas.Type, defs map[string]*schemas.Type) []fieldSpec {
	required := map[string]bool{}
	props := map[string]*schemas.Type{}
	gatherProps(t, defs, props, required)

	var fields []fieldSpec
	for jsonName, prop := range props {
		isReq := required[jsonName]
		fields = append(fields, fieldSpec{
			jsonName:    jsonName,
			goName:      genutil.FormatGoFieldName(export(jsonName)),
			goType:      propGoType(prop, isReq),
			required:    isReq,
			description: description(prop),
		})
	}
	sortFields(fields)
	return fields
}

func gatherProps(t *schemas.Type, defs map[string]*schemas.Type, props map[string]*schemas.Type, required map[string]bool) {
	if t == nil {
		return
	}
	for _, r := range t.Required {
		required[r] = true
	}
	for k, v := range t.Properties {
		if _, exists := props[k]; !exists {
			props[k] = v
		}
	}
	for _, sub := range t.AllOf {
		if sub.Ref != "" {
			if def, ok := defs[refName(sub.Ref)]; ok {
				gatherProps(def, defs, props, required)
			}
		} else {
			gatherProps(sub, defs, props, required)
		}
	}
}

// ── Type resolution ───────────────────────────────────────────────────────────

func propGoType(t *schemas.Type, required bool) string {
	if t == nil {
		return "interface{}"
	}

	if t.Ref != "" {
		return refGoType(t.Ref, required)
	}

	// anyOf with additionalProperties $ref → typed facet container
	for _, ao := range t.AnyOf {
		if ao.AdditionalProperties != nil && ao.AdditionalProperties.Ref != "" {
			if ct := facetContainerMap[refName(ao.AdditionalProperties.Ref)]; ct != "" {
				return ct
			}
		}
	}

	ptype := ""
	if len(t.Type) > 0 {
		ptype = t.Type[0]
	}

	switch ptype {
	case "string":
		if t.Format == "date-time" {
			if required {
				return "time.Time"
			}
			return "*time.Time"
		}
		if len(t.Enum) > 0 {
			return "*EventType"
		}
		if required {
			return "string"
		}
		return "*string"
	case "boolean":
		if required {
			return "bool"
		}
		return "*bool"
	case "integer":
		if required {
			return "int64"
		}
		return "*int64"
	case "array":
		if t.Items != nil && t.Items.Ref != "" {
			return "[]" + refGoElem(t.Items.Ref)
		}
		return "[]interface{}"
	}
	return "interface{}"
}

func refGoType(ref string, required bool) string {
	name := refName(ref)
	goType := schemaToGoName[name]
	if goType == "" {
		goType = name
	}
	if required {
		return goType
	}
	return "*" + goType
}

func refGoElem(ref string) string {
	name := refName(ref)
	if goType := schemaToGoName[name]; goType != "" {
		return goType
	}
	return name
}

func refName(ref string) string {
	const marker = "/$defs/"
	if idx := strings.LastIndex(ref, marker); idx >= 0 {
		return ref[idx+len(marker):]
	}
	return ref
}

// ── Helpers ───────────────────────────────────────────────────────────────────

func description(t *schemas.Type) string {
	if t == nil {
		return ""
	}
	return t.Description
}

func oneLine(s string) string {
	return strings.TrimSpace(strings.ReplaceAll(s, "\n", " "))
}

func export(s string) string {
	if s == "" {
		return s
	}
	return strings.ToUpper(s[:1]) + s[1:]
}

func sortFields(fields []fieldSpec) {
	sort.Slice(fields, func(i, j int) bool {
		return fields[i].jsonName < fields[j].jsonName
	})
}
